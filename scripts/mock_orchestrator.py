"""
Mock Orchestrator — right-hand test harness.

Generates TaskMessages with mock fileContent, pushes them to aia-tasks,
then polls aia-status until all responses arrive (or the timeout expires).
Useful for testing the Relay Service in isolation without running the real
CoreBackend + Orchestrator.

Usage:
    # Push one task per agent type for a random document
    python scripts/mock_orchestrator.py

    # Use a specific document ID
    python scripts/mock_orchestrator.py --doc-id abc-123

    # Only push tasks for specific agent types
    python scripts/mock_orchestrator.py --agent-types security technical

    # Send real file content from disk
    python scripts/mock_orchestrator.py --file path/to/document.txt

    # Wait up to 120 s for all results
    python scripts/mock_orchestrator.py --timeout 120
"""

